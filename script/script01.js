function menuOpen() {
    document.querySelector('nav').style.display = "block";
    document.querySelector('.xbtnbox').style.display = "block";
    document.querySelector('.hambtnbox').style.display = "none";
}

function menuClose() {
    document.querySelector('nav').style.display = "none";
    document.querySelector('.xbtnbox').style.display = "none";
    document.querySelector('.hambtnbox').style.display = "block";
}